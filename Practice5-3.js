let output = ''

for (i = 0; i < 9; i++) {
  for(k = 0; k < 8-i; k++){
    output +=' '
  }
  for (j = 0; j < (2*i+1); j++){
    output += '*'  
  }
  output += '\n'
}

console.log(output)

